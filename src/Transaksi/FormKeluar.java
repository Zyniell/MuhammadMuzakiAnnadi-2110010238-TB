    private void isitabel() {
        try {
            stmt = con.createStatement();
            rs = stmt.executeQuery("SELECT * FROM brng_keluar");
            
            DefaultTableModel model = new DefaultTableModel();
                TableKeluar.setModel(model);
                model.addColumn("ID Keluar");
                model.addColumn("Tanggal Keluar");
                model.addColumn("Nama Barang");
                model.addColumn("Stok Keluar");
                model.addColumn("Nama Petugas");
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while (rs.next()) {
                Object[] data = {
                  rs.getString("id_keluar"),
                  rs.getString("tgl_keluar"),
                  rs.getString("nm_barang"),
                  rs.getString("stok_keluar"),
                  rs.getString("nm_user")
                };
                model.addRow(data);
                //TableBrng.setModel(model);
            }
        } catch (Exception e) {
            System.err.println("Error : " + e.getMessage());
        }
    }
    
    private void cbb_barang() {
        try {
            stmt = con.createStatement();
            rs = stmt.executeQuery("SELECT * FROM barang");
            
            while (rs.next()) {
                String jns = rs.getString("nm_barang");
                ComboBoxBarang.addItem(jns);
            }
        } catch (Exception e) {
            System.err.println("Error : " + e.getMessage());
        }
        
    }
