    private void BtnTambah1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTambah1ActionPerformed
        try {
            stmt = con.createStatement();
            if(TextIDKeluar.getText().equals("") || ComboBoxBarang.getSelectedItem().equals("Pilih Nama Barang") || 
                    TextStok.getText().equals("") || TextUser.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Isi semua data terlebih dahulu");
                return;
            }
            
            //simpan
            if (BtnTambah1.getText() == "TAMBAH") {
                String cek = "SELECT * FROM brng_keluar WHERE id_keluar = '" + TextIDKeluar.getText() + "'";
                rs = stmt.executeQuery(cek);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "ID User ini Sudah Ada");
                }else{
                    String sql = "INSERT INTO brng_keluar VALUES ('" + TextIDKeluar.getText() + 
                        "','" + TextTanggal.getText() + "','" + ComboBoxBarang.getSelectedItem() + 
                        "','" + TextStok.getText() + "','" + TextUser.getText() + "')";
                    stmt.executeUpdate(sql);                
                
                    JOptionPane.showMessageDialog(null, "Data Berhasil di Input");
                    bersih();
                    isitabel();
                    
                    MainPanel.removeAll();
                    MainPanel.repaint();
                    MainPanel.revalidate();
        
                    MainPanel.add(PanelKeluar);
                    MainPanel.repaint();
                    MainPanel.revalidate();
                }
            }else{
                String update = "UPDATE brng_keluar SET tgl_keluar = '" + TextTanggal.getText() +
                    "', nm_barang = '" + ComboBoxBarang.getSelectedItem() +
                    "', stok_keluar = '" + TextStok.getText() +
                    "', nm_user = '" + TextUser.getText() +
                    "' WHERE id_keluar = '" + TextIDKeluar.getText() + "'";
                stmt.executeUpdate(update);
                
                JOptionPane.showMessageDialog(null, "Data Berhasil di Ubah");
                bersih();
                isitabel();
                
                BtnHapus.setVisible(false);
                BtnBatal.setVisible(false);
                jLabel2.setText("TAMBAH DATA BARANG MASUK");
                BtnTambah.setText("TAMBAH");
                
                BtnTambah1.setText("TAMBAH");
                BtnHapus1.setVisible(true);
        
                MainPanel.removeAll();
                MainPanel.repaint();
                MainPanel.revalidate();
        
                MainPanel.add(PanelKeluar);
                MainPanel.repaint();
                MainPanel.revalidate();
            }
        } catch (Exception e) {
            System.err.println("Error : " + e.getMessage());
        }
    }//GEN-LAST:event_BtnTambah1ActionPerformed

