    private void isitabel() {
        try {
            stmt = con.createStatement();
            rs = stmt.executeQuery("SELECT * FROM mrk_barang");
            
            DefaultTableModel model = new DefaultTableModel();
                TableMerek.setModel(model);
                model.addColumn("ID Merek");
                model.addColumn("Nama Merek");
                
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while (rs.next()) {
                Object[] data = {
                  rs.getString("id_merek"),
                  rs.getString("nm_merek"),
                };
                model.addRow(data);
            }
            
        } catch (Exception e) {
            System.err.println("Error : " + e.getMessage());
        }
    }
